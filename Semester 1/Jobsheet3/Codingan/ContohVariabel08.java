public class ContohVariabel08 {
    public static void main(String[] args) {
        String hobi = "Tidur tiduran";
        boolean isPandai = true;
        char jenisKelamin = 'L';
        byte umur = 18;
        double $ipk = 3.24, tinggi = 1.72;
        System.out.println(hobi);
        System.out.println("Apakah saya pandai? " + isPandai);
        System.out.println("Jenis Kelamin: " + umur);
        System.out.println("Umurku saat ini: " + jenisKelamin);
        System.out.println(String.format("Saya beripk %s, dengan tinggi badan %s", $ipk, tinggi));
    }
}

